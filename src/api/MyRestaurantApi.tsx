  const createMyRestaurantRequest = async (restaurantFormData: FormData) => {
    const accessToken = await getAccessTokenSilently();
    //logging the restaurantFormData object
    // for (let [key, value] of restaurantFormData.entries()) {
    //   // Check if the value is a File object or a string
    //   if (value instanceof File) {
    //     console.log(`${key}:`, value.name, value.type);
    //   } else {
    //     console.log(`${key}:`, value);
    //   }
    // }

    const response = await fetch(`${API_BASE_URL}/api/my/restaurant`, {
      method: "POST",
      headers: {
        Authorization: `Bearer ${accessToken}`,
      },
      body: restaurantFormData,
    });

    console.log("Response Status:", response.status);
    console.log("Response Headers:", [...response.headers]);
    console.log("Response Body:", await response.text());

    if (!response.ok) {
      throw new Error("Failed  to create restaurant!");
    }

    //whenever we create a restaurant the backend will return all the restaurant data
    //any component can use this if they need it
    return response.json();
  };

